package School.managment;

public class Person {
    public String name;
    protected int age;

    public Person(String name, int age) {
        this.name = this.name;
        this.age = this.age;
    }

    public void displayDetails() {
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
    }
}
